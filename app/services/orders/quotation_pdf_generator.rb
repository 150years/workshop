          price(subtotal - labor_total_cents),
          price(vat),
          price(grand_total)
        ].join("\n")
        # –î–æ–±–∞–≤–ª—è–µ–º —Å—Ç—Ä–æ–∫—É –≤ —Ç–∞–±–ª–∏—Ü—É
        data.size
        data << [
          { content: summary_left, align: :right, colspan: 5, inline_format: true },
